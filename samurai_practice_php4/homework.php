     * 繰り返すたびに $yourZyanken は変わらければならない
     *  $yourZyanken = ['グー', 'チョッキ', 'パー'][rand(0, 2)]
     * 上のコードはじゃんけんをランダムに出すコードである。
     */

    for( $i = 1 ; $i <= 100 ; $i++ ) {
        $yourZyanken = ['グー', 'チョキ', 'パー'][rand(0, 2)];
        if ( $yourZyanken === "グー") {
            echo $i ."回目　＝＞　" ."　あいこ　" ."<br>" ;
