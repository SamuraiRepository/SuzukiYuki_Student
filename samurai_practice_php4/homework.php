                echo $i ."回目　＝＞　" ."　負け　" ."<br>" ;
            } else {
                echo $i ."回目" ." 　変なのは出さないで！！　" ."<br>" ;
            }
    }

    /**
     * １回目～100回までを設定
     * グーを出した場合の回答を用意　→　あいこ
